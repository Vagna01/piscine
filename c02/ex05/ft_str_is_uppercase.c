int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			i = 0;
			return (i);
		}
		else
		{
			i++;
		}
	}
	i = 1;
	return (i);
}
